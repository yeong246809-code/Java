package test2;

import java.util.Scanner;

class Solution {
    public int solution(int num1, int num2) {
        
    	Scanner sc = new Scanner(System.in);
    	
    	num1 = sc.nextInt();
    	num2 = sc.nextInt();
    	
    	int answer = num1 * num2;
    	
    	
    	
        return answer;
        
    }
   
}